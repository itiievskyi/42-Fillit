#include "fillit.h"

static void	get_touches(char *s, int p[], int i)
{
	if (s[i - 1] == '#')
		p[7]++;
	if (s[i - 5] == '#')
		p[7]++;
	if (s[i + 1] == '#')
		p[7]++;
	if (s[i + 5] == '#')
		p[7]++;
}

void		ft_check_tetr(char *s, int p[], int i, int x)
{
	while (s[i] != '\0')
	{
		if (i % 21 == 0)
		{
			x = 0;
			p[5] = 0;
			p[7] = 0;
		}
		if (s[i] == '#')
		{
			if (s[i - 1] != '#' && s[i + 1] != '#' && (s[i - 5] != '#'
				|| (s[i - 5] == '#' && x - 5 < 0)) && (s[i + 5] != '#'
				|| (s[i + 5] == '#' && x + 5 > 19)))
				ft_quit(s, p);
			get_touches(s, p, i);
			p[5]++;
			if (p[5] == 4 && p[7] < 6)
				ft_quit(s, p);
		}
		i++;
		x++;
	}
}
